# 2. API Keys ‡∑É‡∑Ñ Model ‡∂≠‡∑ù‡∂ª‡∑è ‡∂ú‡∑ê‡∂±‡∑ì‡∂∏ (Error Handling ‡∑É‡∂∏‡∂ü)
def load_model():
    # Secrets ‡∂≠‡∑î‡∑Ö ‡∂á‡∂≠‡∑í Keys ‡∑É‡∑ú‡∂∫‡∑è ‡∂ú‡∑ê‡∂±‡∑ì‡∂∏
    keys = []
    for i in range(1, 6):
        key_name = f"GEMINI_API_KEY_{i}"
        if key_name in st.secrets:
            keys.append(st.secrets[key_name])
    
    if not keys and "GEMINI_API_KEY" in st.secrets:
        keys.append(st.secrets["GEMINI_API_KEY"])

    if not keys:
        st.error("‚ùå API Keys ‡∂ö‡∑í‡∑É‡∑í‡∑Ä‡∂ö‡∑ä ‡∑Ñ‡∂∏‡∑î ‡∂±‡∑ú‡∑Ä‡∑ì‡∂∫. ‡∂ö‡∂ª‡∑î‡∂´‡∑è‡∂ö‡∂ª Secrets ‡∂¥‡∂ª‡∑ì‡∂ö‡∑ä‡∑Ç‡∑è ‡∂ö‡∂ª‡∂±‡∑ä‡∂±.")
        return None

    selected_key = random.choice(keys)
    genai.configure(api_key=selected_key)

    # 404 ‡∂Ø‡∑ù‡∑Ç‡∂∫ ‡∂∏‡∂ü‡∑Ñ‡∂ª‡∑Ä‡∑è ‡∂ú‡∑ê‡∂±‡∑ì‡∂∏‡∂ß ‡∂±‡∑í‡∑Ä‡∑ê‡∂ª‡∂Ø‡∑í ‡∂∏‡∑è‡∂Ø‡∑í‡∂Ω‡∑í ‡∂±‡∑è‡∂∏‡∂∫‡∂±‡∑ä ‡∂¥‡∂ª‡∑ì‡∂ö‡∑ä‡∑Ç‡∑è ‡∂ö‡∑í‡∂ª‡∑ì‡∂∏
    # models/gemini-1.5-flash ‡∑Ä‡∑ô‡∂±‡∑î‡∑Ä‡∂ß gemini-1.5-flash ‡∂Ω‡∑ô‡∑É ‡∂∑‡∑è‡∑Ä‡∑í‡∂≠‡∂∫ ‡∑Ä‡∂©‡∑è‡∂≠‡∑ä ‡∑É‡∑î‡∂Ø‡∑î‡∑É‡∑î‡∂∫‡∑í
    model_names = ['gemini-1.5-flash', 'gemini-1.5-pro', 'gemini-pro']
    
    for name in model_names:
        try:
            model = genai.GenerativeModel(name)
            # ‡∂ö‡∑î‡∂©‡∑è ‡∂¥‡∂ª‡∑ì‡∂ö‡∑ä‡∑Ç‡∂´‡∂∫‡∂ö‡∑ä (Test request ‡∂ë‡∂ö‡∂ö‡∑ä ‡∂±‡∑ú‡∂∫‡∑Ä‡∑è ‡∑É‡∂ª‡∂Ω‡∑Ä model ‡∂ë‡∂ö return ‡∂ö‡∂ª‡∂∫‡∑í)
            return model
        except:
            continue
    return None

model = load_model()

# 3. AI ‡∂¥‡∑ä‚Äç‡∂ª‡∂≠‡∑í‡∂†‡∑è‡∂ª ‡∂Ω‡∂∂‡∑è ‡∂ú‡∑ê‡∂±‡∑ì‡∂∏ (Caching ‡∑É‡∂∏‡∂ü)
@st.cache_data(show_spinner=False)
def get_pali_analysis(pali_input):
    if model:
        prompt = f"""
        As a world-class Pali Philologist and Tipitaka scholar:
        1. Translate this Pali text into BOTH Sinhala and English: "{pali_input}"
        2. Identify the exact source in the Tipitaka.
        3. Provide a DEEP GRAMMATICAL ANALYSIS (Padavigga) in a table.
        4. List relevant references to SuttaCentral.net or Tipitaka.lk.
        5. Explain the context (Nidana).
        """
        try:
            response = model.generate_content(prompt)
            return response.text
        except Exception as e:
            return f"AI ‡∂¥‡∑ä‚Äç‡∂ª‡∂≠‡∑í‡∂†‡∑è‡∂ª‡∂∫‡∑ö ‡∂Ø‡∑ù‡∑Ç‡∂∫‡∂ö‡∑í: {str(e)}"
    return "AI ‡∂∏‡∑è‡∂Ø‡∑í‡∂Ω‡∑í‡∂∫ ‡∑É‡∂ö‡∑ä‚Äç‡∂ª‡∑ì‡∂∫ ‡∂±‡∑ê‡∂≠."

