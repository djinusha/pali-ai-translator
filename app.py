import streamlit as st
import google.generativeai as genai
from streamlit_antd_components import button # ‡∂∏‡∑ô‡∂∫ ‡∂Ö‡∑Ä‡∑Å‡∑ä‚Äç‡∂∫ ‡∂±‡∑ê‡∂≠, ‡∂Ö‡∂¥‡∑í ‡∑É‡∂ª‡∂Ω‡∑Ä ‡∑É‡∑è‡∂Ø‡∂∏‡∑î

# ‡∂¥‡∑í‡∂ß‡∑î‡∑Ä‡∑ö ‡∑É‡∑ê‡∂ö‡∑É‡∑î‡∂∏‡∑ä
st.set_page_config(page_title="AI ‡∂¥‡∑è‡∂Ω‡∑í ‡∂¥‡∂ª‡∑í‡∑Ä‡∂ª‡∑ä‡∂≠‡∂ö‡∂∫", page_icon="‚ò∏Ô∏è")

def get_working_model():
    try:
        available_models = [m.name for m in genai.list_models() if 'generateContent' in m.supported_generation_methods]
        for preferred in ['models/gemini-1.5-flash', 'models/gemini-1.5-flash-latest', 'models/gemini-pro']:
            if preferred in available_models:
                return preferred
        return available_models[0] if available_models else None
    except:
        return None

# API ‡∑É‡∂∏‡∑ä‡∂∂‡∂±‡∑ä‡∂∞‡∂≠‡∑è‡∑Ä‡∂∫
if "GEMINI_API_KEY" in st.secrets:
    genai.configure(api_key=st.secrets["GEMINI_API_KEY"])
    model_id = get_working_model()
    model = genai.GenerativeModel(model_id) if model_id else None

st.title("‚ò∏Ô∏è AI ‡∂¥‡∑è‡∂Ω‡∑í ‡∂¥‡∂ª‡∑í‡∑Ä‡∂ª‡∑ä‡∂≠‡∂ö‡∂∫")
st.markdown("---")

# --- ‡∂¥‡∑è‡∂Ω‡∑í ‡∑Ä‡∑í‡∑Å‡∑ö‡∑Ç ‡∂Ö‡∂ö‡∑î‡∂ª‡∑î ‡∂¥‡∑î‡∑Ä‡∂ª‡∑î‡∑Ä (Pali Keyboard) ---
st.write("‡∑Ä‡∑í‡∑Å‡∑ö‡∑Ç ‡∂Ö‡∂ö‡∑î‡∂ª‡∑î ‡∂Ö‡∑Ä‡∑Å‡∑ä‚Äç‡∂∫ ‡∂±‡∂∏‡∑ä ‡∂∏‡∑ô‡∂≠‡∑ê‡∂±‡∑í‡∂±‡∑ä ‡∂≠‡∑ù‡∂ª‡∂±‡∑ä‡∂±:")
special_chars = ['ƒÅ', 'ƒ´', '≈´', '·πÉ', '·πá', '·πá·∏ç', '·∏∑', '·π≠', '·∏ç', '√±', '·πÖ']
cols = st.columns(len(special_chars))

# session_state ‡∂∑‡∑è‡∑Ä‡∑í‡∂≠‡∑è ‡∂ö‡∂ª ‡∂ß‡∂∫‡∑í‡∂¥‡∑ä ‡∂ö‡∂ª‡∂± ‡∂Ø‡∑ö ‡∂≠‡∂∂‡∑è ‡∂ú‡∑ê‡∂±‡∑ì‡∂∏
if 'pali_input' not in st.session_state:
    st.session_state.pali_input = ""

def add_char(char):
    st.session_state.pali_input += char

for i, char in enumerate(special_chars):
    if cols[i].button(char):
        add_char(char)

# ‡∂¥‡∑è‡∂Ω‡∑í ‡∑Ä‡∑è‡∂ö‡∑ä‚Äç‡∂∫‡∂∫ ‡∂á‡∂≠‡∑î‡∑Ö‡∂≠‡∑ä ‡∂ö‡∂ª‡∂± ‡∂ö‡∑ú‡∂ß‡∑î‡∑Ä
pali_text = st.text_area("‡∂¥‡∑è‡∂Ω‡∑í ‡∑Ä‡∑è‡∂ö‡∑ä‚Äç‡∂∫‡∂∫ ‡∂∏‡∑ô‡∑Ñ‡∑í ‡∂á‡∂≠‡∑î‡∑Ö‡∂≠‡∑ä ‡∂ö‡∂ª‡∂±‡∑ä‡∂±:", value=st.session_state.pali_input, key="main_input")

# Clear ‡∂∂‡∑ú‡∂≠‡∑ä‡∂≠‡∂∏
if st.button("‡∂¥‡∑í‡∂ª‡∑í‡∑É‡∑í‡∂Ø‡∑î ‡∂ö‡∂ª‡∂±‡∑ä‡∂± (Clear)"):
    st.session_state.pali_input = ""
    st.rerun()

st.markdown("---")

if st.button("‡∂¥‡∂ª‡∑í‡∑Ä‡∂ª‡∑ä‡∂≠‡∂±‡∂∫ ‡∂ö‡∂ª‡∂±‡∑ä‡∂±", type="primary"):
    if pali_text:
        with st.spinner('‡∂¥‡∂ª‡∑í‡∑Ä‡∂ª‡∑ä‡∂≠‡∂±‡∂∫ ‡∑Ä‡∑ô‡∂∏‡∑í‡∂±‡∑ä ‡∂¥‡∑Ä‡∂≠‡∑ì...'):
            try:
                prompt = f"As a Pali scholar, translate this to Sinhala and English with word meanings: {pali_text}"
                response = model.generate_content(prompt)
                st.markdown("### üìù ‡∂¥‡∑ä‚Äç‡∂ª‡∂≠‡∑í‡∂µ‡∂Ω‡∂∫:")
                st.write(response.text)
            except Exception as e:
                st.error(f"‡∂¥‡∂ª‡∑í‡∑Ä‡∂ª‡∑ä‡∂≠‡∂±‡∂∫ ‡∂Ö‡∑É‡∑è‡∂ª‡∑ä‡∂Æ‡∂ö ‡∑Ä‡∑í‡∂∫: {e}")
    else:
        st.warning("‡∂ö‡∂ª‡∑î‡∂´‡∑è‡∂ö‡∂ª ‡∂¥‡∑è‡∂Ω‡∑í ‡∑Ä‡∑è‡∂ö‡∑ä‚Äç‡∂∫‡∂∫‡∂ö‡∑ä ‡∂á‡∂≠‡∑î‡∑Ö‡∂≠‡∑ä ‡∂ö‡∂ª‡∂±‡∑ä‡∂±.")
